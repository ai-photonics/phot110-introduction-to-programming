# ex 1
# Use a while loop to plot numbers 4 to 12 (separately printing them one by one)
# Syntax:
# while <condition>:
#   statements (print the current number to the console)
#
# Possible solution 1
x = 3
while x < 12:
    x = x + 1
    print(x)

# Possible solution 2
x = 4
while x <= 12:
    print(x)
    x = x + 1
